    // Add logic to manage zoo upgrades and interactive features
    const income = 0; // Example variable to track income generated by critters
    const upgrades = {}; // Object to store available and purchased upgrades

    function purchaseUpgrade(upgradeName) {
        if (income >= upgrades[upgradeName].cost) {
            income -= upgrades[upgradeName].cost;
            upgrades[upgradeName].purchased = true;
            console.log(`Purchased ${upgradeName}`);
        } else {
            console.log("Insufficient funds");
        }
    }

    // Example upgrade
    upgrades['newFeature'] = {
        cost: 100,
        purchased: false
    };

    module.exports = { income, upgrades, purchaseUpgrade };
    